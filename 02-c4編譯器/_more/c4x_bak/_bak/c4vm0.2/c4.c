int main() // 主程式
{
  int *pc, *bp, *sp;

  pc = e = code;  
  memset(code, 0, POOL_SIZE);
  memset(sym, 0, POOL_SIZE*sizeof(token_t));

  pc = e;
// 1: int main()
// 2: {
// 3:   printf("hello, world\n");
  *e++ = ENT; *e++ = 0;
  *e++ = IMM; *e++ = (int) "hello, world\n";
  *e++ = PSH;
  *e++ = PRTF;
  *e++ = ADJ; *e++ = 1;
// 4:   exit();
  *e++ = EXIT;
// 5: }
  bp = sp = (int *)((int)stack + POOL_SIZE); // setup stack
  return run(pc, bp, sp);
}
